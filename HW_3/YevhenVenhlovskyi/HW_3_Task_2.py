x = input (f'Enter a 4 numbers integer number: ')

#printing the number in the reverse consistency
print ("Your number in the reverse consistency:", x [::-1])

#sorting number's elements
y=sorted(x)
z=int(y[0]+y[1]+y[2]+y[3])
print("Your number in the sorted consistency:", z)

# multiplication of number's elements
m=int(y[0])*int(y[1])*int(y[2])*int(y[3])
print("Multiplication of number's elements:", m)