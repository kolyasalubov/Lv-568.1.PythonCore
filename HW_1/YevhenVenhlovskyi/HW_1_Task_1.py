a=3
b=2
print(a+b) # add
print(a-b) # subtract
print(a*b) # multiply
print(a**b) # Exponent - left operand raised to the power of right
print(a/b) # divide
print(a//b) # Floor division - division that results into whole number adjusted to the left in the number line
print(a%b) # Modulus - remainder of the division of left operand by the right