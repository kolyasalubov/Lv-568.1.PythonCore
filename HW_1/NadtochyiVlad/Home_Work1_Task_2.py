first_value = int(input("Enter the first value "))
second_value = int(input("Enter the second value "))

print(f"First value {first_value } + second value {second_value} = sum", first_value + second_value)
print(f"First value {first_value } - second value {second_value} = subtraction", first_value  - second_value)
print(f"First value {first_value } * second value {second_value} = multiplication", first_value  * second_value)
print(f"First value {first_value } / second value {second_value} = division", first_value  / second_value)
print(f"First value {first_value } ** second value {second_value} = exponentiation", first_value  ** second_value)
