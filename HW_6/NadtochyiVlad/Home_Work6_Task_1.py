def max_of_two (first_number, second_number):

    """ 
    function name max_of_two
    input parametrs two: first_number and second_number
    return largest number of two numbers 
    """

    return max(first_number, second_number)

print("Largest number of two numbers: ",max_of_two(int(input("Enter first number: ")),int(input("Enter second number: "))))