from math import pi, pow

def calc_rectangle(a, b):
    print('The square of ​​the rectangle is equal to: ', round(a * b, 3))

def calc_triangle(a, h):
    print('The square of ​​the triangle is equal to: ', round(0.5 * h * a, 3))

def calc_circle(R):
    print('The square of ​​the circle is equal to: ', round(pi * pow(R, 2), 3))
