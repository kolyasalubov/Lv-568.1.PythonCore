from math import pow
from math import pi

def circle (r):
    """
    a function that calculates the area of ​​a circle
    """
    return (pow(r,2)*pi)

def rectangle (a,b):
    """
    a function that calculates the area of ​​a rectangle
    """
    square_rectangle=a*b
    return square_rectangle

def triangle(a,h):
    """
    a function that calculates the area of ​​a triangle
    """
    square_triangle=0.5*a*h
    return square_triangle
